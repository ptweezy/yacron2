"""In-house cron expression engine.

:class:`CronTab` parses the crontab dialect cronstable has always accepted and
answers the two questions the scheduler asks: "when does this fire next?"
(:meth:`CronTab.next`) and "does this instant match?" (:meth:`CronTab.test`).
The tooling around the scheduler gets three more: :meth:`CronTab.prev` (the
most recent past occurrence), :meth:`CronTab.occurrences` (iterate the exact
instants the scheduler would fire) and read-only properties exposing the
parsed field sets, so linters and describers work from ground truth instead
of re-parsing the text.

It replaces the third-party ``crontab`` (parse-crontab) dependency cronstable
previously leaned on, moving the one piece of syntax every job depends on
into the tree.  Compatibility with that library is not aspirational -- it is
enforced by golden vectors recorded from it across 180+ expressions and
fixed instants (DST transitions, leap days, month/year boundaries, the year
cap, microsecond nows, ambiguous-time folds): see
``tests/data/cron_golden.json``, regenerated by
``tests/gen_cron_golden.py`` and replayed by ``tests/test_cronexpr.py``.
Three additive day-form families (``L-<n>``, ``<n>W``/``LW`` and
``<d>#<n>``, described below) postdate the swap: each was a parse error in
the legacy dialect, so no legacy schedule changed meaning, and their
vectors are recorded from THIS engine (the ``"extension": true`` entries in
the same file) to pin the extended behavior just as hard.

The dialect (the "Schedules and Timezones" wiki page is the user
documentation):

- 5 fields ``minute hour dayOfMonth month dayOfWeek`` (implicit second ``0``,
  any year); 6 fields add a trailing ``year`` column; 7 fields add a leading
  ``second`` column too.
- Per field: values, lists (``1,15,30``), ranges (``1-5``), steps (``*/5``,
  ``1-9/2``, and ``5/15`` -- a bare start with a step runs to the field's
  end), month and weekday names (``jan``, ``mon-fri``), case-insensitive.
- Weekdays are ``0``-``7`` with BOTH 0 and 7 meaning Sunday; a range ending
  in 0 wraps its end to 7 (so ``sat-sun`` and ``6-0`` work); star/step
  expansion runs 0-6.
- ``L`` alone in day-of-month is the month's last day; ``L<n>`` (or
  ``L<a>-<b>``) in day-of-week is the month's LAST such weekday (``L5`` =
  last Friday).  No other field takes an ``L``.
- Three additive families extend the legacy dialect (each was a parse error
  before, so no pre-existing schedule changed meaning).  In day-of-month:
  ``L-<n>`` counts back from the month's final day (``L-3`` = three days
  before it; n runs 1-30), ``<n>W`` is the weekday (Mon-Fri) nearest day
  ``n`` within the same month (a Saturday 15th resolves to Friday the
  14th, a Sunday 15th to Monday the 16th; at the month's edges the fire
  flips inward instead of escaping, so ``1W`` on a Saturday 1st is Monday
  the 3rd and ``31W`` on a Sunday 31st is Friday the 29th; a day the month
  never reaches, like ``31W`` in April, does not fire that month), and
  ``LW`` is the month's last weekday.  In day-of-week: ``<d>#<n>`` is the
  month's n-th such weekday (``5#3`` = third Friday, ``mon#1`` = first
  Monday; ``d`` is one weekday value, numeric 0-7 or a name, 7 folding to
  Sunday; ``n`` runs 1-5; a month with no n-th such weekday does not
  fire).  All are ordinary list items (``1,15W,L``; ``mon#1,L5``) and,
  like every day form, participate in the both-restricted AND rule below.
- ``@yearly``/``@annually``/``@monthly``/``@weekly``/``@daily``/``@hourly``
  expand to their classic five-field forms.  ``@reboot`` never reaches this
  parser (the scheduler gives it meaning) and ``@midnight`` is rewritten to
  ``@daily`` by the classic-crontab loader, exactly as before.
- ``?`` standing alone in day-of-month or day-of-week reads as ``*``: the
  Quartz spelling of "unrestricted", accepted so a 7-field Quartz expression
  (whose column layout matches this dialect's) parses verbatim.  Anywhere
  else ``?`` stays an error, and parse errors whose text smells of Quartz
  (``#``, ``W``, the seconds-first 6-field layout) carry a hint naming that
  dialect instead of leaving the user to guess.
- ``H`` (Jenkins-style) in any field but the year: a value hashed from the
  job's name, stable across restarts, reloads and hosts, so a fleet of
  ``H * * * *`` jobs spreads across the hour instead of stampeding at :00
  while each job keeps ONE predictable slot (random jitter would break
  late-run detection; a hashed slot does not).  Forms: bare ``H`` (whole
  field range), ``H(a-b)`` (a numeric range), ``H/n`` and ``H(a-b)/n``
  (every ``n`` starting at a hashed offset); in day-of-month the rangeless
  forms (bare ``H`` and ``H/n``) hash over 1-28 so a short month is never
  silently skipped.  The seed is the
  ``hash_key`` handed to :class:`CronTab` (the job name everywhere
  cronstable constructs one), salted per field so ``H H * * *`` picks
  uncorrelated minute and hour; renaming a job re-hashes its slots.  A
  context that supplies no ``hash_key`` rejects ``H`` with a clear error.
  :attr:`CronTab.resolved_source` is the expression with every ``H``
  replaced by the values it hashed to.
- When day-of-month AND day-of-week are both restricted, a day must satisfy
  BOTH (``0 0 13 * 5`` = Friday the 13th).  This is parse-crontab's rule,
  deliberately preserved (Vixie cron fires on either) so that no existing
  schedule changes meaning.
- The ``year`` column accepts 1970-2099.  :meth:`test` does not bound the
  year when the column is ``*`` or absent, but :meth:`next` never searches
  past 2099 and returns ``None`` for a schedule with no remaining occurrence
  (``0 0 30 2 * *``, or a fixed past year) -- callers drop such jobs from
  the fire index.

Time model (also preserved): :meth:`next` is strictly future -- an instant
that matches "now" yields the following occurrence, so index seeding stays
strictly-future.  The search runs in the CIVIL clock of the ``now`` it is
given: for an aware ``now`` the matching wall-clock instant is resolved back
in ``now``'s own zone (``fold=0``) and the returned delay is the true elapsed
seconds across any UTC-offset change.  Concretely, on a spring-forward day a
``0 3 * * *`` job one civil minute after 01:59 fires in 60 real seconds, and
an ambiguous fall-back wall time fires on its first occurrence only.
:meth:`test` reads the civil fields of the datetime it is handed (aware or
naive) and ignores microseconds.

:meth:`prev` mirrors :meth:`next` into the past: seconds since the most
recent occurrence STRICTLY before ``now``, ``None`` when nothing occurs at or
after 1970 (the year column's floor).  :meth:`occurrences` iterates forward
and, for an aware start, steps through REAL instants the way the scheduler
does: a wall time a spring-forward transition skips is yielded once at its
shifted label, and a repeated fall-back wall time is yielded on its first
occurrence only, so consumers never see one real instant twice.
"""

